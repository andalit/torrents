CREATE TABLE `bb_bulkmail` (
  `bulk_id` int(10) NOT NULL auto_increment,
  `bulk_complete` tinyint(1) NOT NULL default '0',
  `last_user_id` mediumint(8) NOT NULL default '0',
  `mail_subject` varchar(255) NOT NULL,
  `mail_body` text,
  `group_id` mediumint(8) NOT NULL default '-1',
  PRIMARY KEY  (`bulk_id`)
) ENGINE=InnoDB AUTO_INCREMENT=4 DEFAULT CHARSET=cp1251;